package openup.client.batch.api;

import java.util.List;
import java.util.Map;

/**
 *
 * @author FOXCONN
 */
public class StepExecutionDetail {
    private long stepExecutionId;
    private long executionId;
    private long instanceId;
    private String stepName;
    private String batchStatus;
    private String startTime;
    private String endTime;
    private String exitStatus;
    private Map<String, Object> metrics;
    private List<PartitionedStep> partitions;

    public long getStepExecutionId() {
        return stepExecutionId;
    }

    public void setStepExecutionId(long stepExecutionId) {
        this.stepExecutionId = stepExecutionId;
    }

    public long getExecutionId() {
        return executionId;
    }

    public void setExecutionId(long executionId) {
        this.executionId = executionId;
    }

    public long getInstanceId() {
        return instanceId;
    }

    public void setInstanceId(long instanceId) {
        this.instanceId = instanceId;
    }

    public String getStepName() {
        return stepName;
    }

    public void setStepName(String stepName) {
        this.stepName = stepName;
    }

    public String getBatchStatus() {
        return batchStatus;
    }

    public void setBatchStatus(String batchStatus) {
        this.batchStatus = batchStatus;
    }

    public String getStartTime() {
        return startTime;
    }

    public void setStartTime(String startTime) {
        this.startTime = startTime;
    }

    public String getEndTime() {
        return endTime;
    }

    public void setEndTime(String endTime) {
        this.endTime = endTime;
    }

    public String getExitStatus() {
        return exitStatus;
    }

    public void setExitStatus(String exitStatus) {
        this.exitStatus = exitStatus;
    }

    public Map<String, Object> getMetrics() {
        return metrics;
    }

    public void setMetrics(Map<String, Object> metrics) {
        this.metrics = metrics;
    }

    public List<PartitionedStep> getPartitions() {
        return partitions;
    }

    public void setPartitions(List<PartitionedStep> partitions) {
        this.partitions = partitions;
    }
}
